<h3>Small Cell as a Service connects crowds at Wasps' Ricoh Arena</h3>
<p>Ericsson has become the connectivity partner for the Ricoh Arena, in Coventry, England
As a result, visitors to the stadium complex will enjoy carrier-grade Wi-Fi connectivity and value-added services accessible via a mobile app
Ericsson will provide a complete, managed solution, including planning, design, implementation, integration, optimization and maintenance services
Ericsson (NASDAQ: ERIC) has become the connectivity partner for the Ricoh Arena stadium in Coventry, England, home to Aviva Premiership rugby team Wasps and Wasps Netball. Ericsson will provide enhanced connectivity to the stadium via a Wi-Fi network, using the Small Cell as a Service business model. As a result, visitors to the stadium will enjoy carrier-grade Wi-Fi connectivity and value-added services accessible via a mobile app. Under the terms of the 10-year agreement, Ericsson will provide Wasps and the Ricoh Arena with a complete, managed solution, including planning, design, implementation, integration, optimization and maintenance services.
The Ricoh Arena includes a 32,609-seater sports stadium, 20,000 square meters of events space - including a 6,000 square meter exhibition hall - and a 121-bedroom hotel. Most of the more than 1.2 million people who visit the Ricoh Arena each year use smartphones to share photos and videos via social media, often stretching network capacity to the limit. To ensure these visitors can enjoy a seamless digital experience, Ericsson will design, plan, build and optimize a carrier-grade Wi-Fi access network, and then manage it on behalf of the stadium owner - Wasps Holdings Ltd. The Ericsson Networked Event platform will also be used to facilitate the creation of an app that provides location-specific news, information and value-added services.
David Armstrong, Wasps Group CEO, says: "It has long been our ambition to ensure that the Ricoh Arena is a venue that ensures high-performing technology, connectivity and interaction are at the forefront of our customers' experience. Announcing this trailblazing relationship with a globally recognized partner such as Ericsson is very exciting and will ensure that we realize our ambition to compete with the best and lead in our respective industry sectors."
Valter D'Avino, Head of Region Western & Central Europe at Ericsson, says: "We believe Small Cell as a Service is the most efficient, cost-effective means of enhancing connectivity in stadiums. We're pleased that Wasps has become the first rugby club to take advantage of Small Cell as a Service, and we have some very exciting ideas about how to evolve our new partnership in the years to come by introducing other innovative solutions from our portfolio. Together we will make the Ricoh Arena one of the most technologically advanced venues in the United Kingdom."
As part of the agreement, the Ricoh Arena's exhibition venue - which regularly hosts sports fan villages, concerts for up to 12,000 people, sports events and tradeshows - will be re-branded as the Ericsson Exhibition Hall.
 
