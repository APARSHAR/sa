<h3>Eastern Time: when ICT policy goes global</h3>
<p>ICT has become a defining aspect of modern societies. It underpins daily life and work in all advanced economies, and is an increasingly important driver of socio-economic transformation in developing countries.
The extraordinary proliferation and complexity of ICT creates an unprecedented challenge for regulators, who are struggling to develop horizontal, future-proof policies that enable digital-led growth whilst offsetting risks like crime facilitation and breaches of privacy. In addition, regulators have yet to crack the problem of keeping legal frameworks relevant and effective when ICT is constantly disrupting the economic status quo.
Until now, the focus of the policy debate in Europe has been three-fold. First, interested parties are analysing how market and sectoral developments in the US are challenging existing laws at home and abroad. Second, the debate is focused on the impact of EU policy developments as a potential precursor to international ICT rules. Third, there is speculation about the impact of a widening divide between Brussels and Washington over differences in ICT policy, and particularly around the perceived targeting of US tech giants by EU decision-makers.
Although undoubtedly important in the global regulatory context, an exclusively transatlantic focus is leading businesses and authorities to neglect the policy impact of economically and politically important countries in Asia.
This article adds a new dimension to the debate by comparing political approaches and legislative agendas in China, Japan and South Korea. It is structured around four key pillars – Politics, Principles, Power and Priorities – and concludes with an analysis of trends shaping policy in all three countries and what they mean for ICT policy formulation globally.
Macro considerations
This article rests on three important considerations:
First, China, Japan and South Korea have emerged as major economic powers that can compete with the traditional titans – the US, Europe and Russia. China is second only to the US in GDP terms, and Japan comes third despite trailing China by a significant amount [1]. South Korea is fast becoming a worthy competitor in its own right, jumping to 11th place in World Economic League Tables ahead of European countries like Spain (14th), the Netherlands (17th) and Sweden (23rd). International Monetary Fund (IMF) data show that all three are set to enjoy continued growth in real GDP terms to 2020. Despite a slowdown in China, the country is still expected to overtake the Western superpowers quicker than expected – by 2025.
Second, all three countries are important ICT players. In 2013, China was the top exporter of ICT goods, accounting for 32 percent of the first 10 countries’ combined exports, and 6th in the export of ICT services. By the same parameters, South Korea and Japan accounted for 7 percent and 4 percent of exported ICT goods respectively, and slightly less for ICT services [2]. In particular, South Korea is considered one of, if not the most advanced ICT economy in the world, based on international indicators like productivity, tech density and patent filings [3]. It is consistently ranked first for business expenditure in R&D, has produced global ICT companies, and comes in 1st or 2nd for fastest internet speeds, depending on the source.
Third, we assume that with economic clout comes policy influence. Efforts toward global regulatory convergence are gaining momentum as our societies and economies merge, which in turn increases the impact of policymaking in China, Japan and South Korea on the global regulatory environment.



















