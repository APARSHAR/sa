<h3>Singtel and Ericsson first to showcase 5G in Southeast Asia</h3>
<p>Singtel and Ericsson have joined forces in Singapore to bring 5G innovation to Southeast Asia for the first time with a live demonstration of the potential of 5G.
The “Making 5G Innovation A Reality” demonstration features Ericsson’s 5G Radio Prototypes to showcase some of the ground-breaking capabilities offered by the new technology, achieving a peak throughput of 27.5Gbps and latency as low as 2ms. Singtel and Ericsson also demonstrated the world’s first end to end (from the time the video is captured at the server end to the time it is displayed at the device) low latency live video streaming over 5G.
This milestone is a result of the 5G memorandum of understanding signed by Ericsson and Singtel last year, which involved evaluating and testing technologies that are strong candidates for future 5G standardization.
Singtel and Ericsson are leading the way for 4G and 5G in Singapore, in support of Singapore’s “Smart Nation” vision and the anticipated four-fold growth1 in connected devices As 5G offers greater throughput and lower latency, it will bring more use cases to Internet of Things (IoT), enabling massive connectivity and allowing new applications for consumers and enterprises such as smart vehicles and transport architecture, remote healthcare and new levels of human-IoT interaction.
Mr Tay Soo Meng, Group Chief Technology Officer, Singtel, says: "We strive to upgrade our networks with cutting-edge enhancements, constantly offering our customers all the possibilities that technology brings. Singapore is at the forefront of the most connected cities in the world and now we want to take it to the next level. 5G is very important to the Singtel Group as it will support advanced communication needs. To ensure the Singtel Group continues our technology leadership in the mobile communications domain, we are exploring, studying and trialling pre-5G technologies with Ericsson."
Mr Magnus Ewerbring, Head of Technology Development, Asia-Pacific, Ericsson says; “5G will enable the full potential of Ericsson’s vision of the Networked Society and 5G performance will benefit consumers, business users and entire industries in Singapore. Together with Singtel we have launched many world-first and nation-first achievements with the 4G network since 2011 and we will be together at the forefront of 5G as well.”
The aim is for 5G networks to be highly efficient, faster, support more users, more devices, more services, and new use cases without a corresponding impact on cost or carbon footprint. 5G will revolutionise five key industries including: TV and media; manufacturing; healthcare; telecommunications; and transportation and infrastructure.
Singtel is progressing well on its 5G journey, deploying key pre-5G technologies such as LTE-Advanced (Carrier aggregation, 256QAM) on its live network and taking the next step to LTE Advanced Pro (LAA, NB-IoT). These radio technology upgrades deliver superior connectivity, characterized by faster and more consistent speeds, to its customers.
With 5G expected to be standardized and commercially available by 2020, Ericsson’s 5G Radio Prototypes enable operators, ecosystem partners, academics and analysts to test 5G capabilities live.
Some key capabilities include multipoint connectivity with distributed multiple input, multiple output (MIMO) and 5G-LTE dual connectivity, to ensure a smooth evolution to 5G and rapid adoption of new 5G-based services.
The Ericsson 5G Radio Prototype has demonstrated not only record-breaking throughput, but also the tight interworking between network layers, frequency bands, and technology standards that will be critical to ensuring seamless connectivity for billions of connected devices, machines and things, supporting consumer, business and industrial applications.
1 According to Ericsson Mobility Report June 2016, it is estimated that there will be 4-fold growth in connected devices between 2015 and 2021.
